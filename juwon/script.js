let currentIndex = 0;
let answers = new Array(questions.length).fill(null);

const progressText = document.getElementById("progressText");
const progressFill = document.getElementById("progressFill");
const questionText = document.getElementById("questionText");
const optionButtons = document.querySelectorAll(".option-btn");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");

function renderQuestion() {
  const currentQuestion = questions[currentIndex];
  const progressPercent = ((currentIndex + 1) / questions.length) * 100;

  progressText.textContent = `진행률 바 ${currentIndex + 1} / ${questions.length}`;
  progressFill.style.width = `${progressPercent}%`;
  questionText.textContent = currentQuestion.question;

  optionButtons.forEach((button, index) => {
    button.textContent = currentQuestion.options[index].text;
    button.classList.toggle("selected", answers[currentIndex] === index);
  });

  prevBtn.disabled = currentIndex === 0;
  nextBtn.textContent = currentIndex === questions.length - 1 ? "결과 보기" : "다음 버튼";
}

function selectOption(index) {
  answers[currentIndex] = index;
  renderQuestion();
}

function goPrev() {
  if (currentIndex > 0) {
    currentIndex--;
    renderQuestion();
  }
}

function goNext() {
  if (answers[currentIndex] === null) {
    alert("선택지를 먼저 골라주세요.");
    return;
  }

  if (currentIndex < questions.length - 1) {
    currentIndex++;
    renderQuestion();
  } else {
    showResult();
  }
}

function calculateScores() {
  const scores = {};

  answers.forEach((answerIndex, questionIndex) => {
    const selectedOption = questions[questionIndex].options[answerIndex];

    Object.entries(selectedOption.score).forEach(([type, point]) => {
      scores[type] = (scores[type] || 0) + point;
    });
  });

  return scores;
}

function showResult() {
  const scores = calculateScores();
  const sortedScores = Object.entries(scores).sort((a, b) => b[1] - a[1]);
  const [topType, topScore] = sortedScores[0];

  document.querySelector(".quiz-container").innerHTML = `
    <div class="progress-box">
      <div class="progress-text">테스트 완료</div>
      <div class="progress-bar"><div class="progress-fill" style="width:100%"></div></div>
    </div>

    <section class="result-box">
      <div class="result-title">${topType}</div>
      <div>${resultDescriptions[topType] || "당신만의 학교 생활 유형입니다."}</div>
      <div class="result-score">점수: ${topScore}점</div>
      <br />
      <div>상위 점수</div>
      ${sortedScores.slice(0, 5).map(([type, score]) => `<div>- ${type}: ${score}점</div>`).join("")}
      <br />
      <button class="option-btn" onclick="location.reload()">다시 하기</button>
    </section>
  `;
}

optionButtons.forEach((button, index) => {
  button.addEventListener("click", () => selectOption(index));
});

prevBtn.addEventListener("click", goPrev);
nextBtn.addEventListener("click", goNext);

renderQuestion();
